import { ReactNode, MouseEvent } from 'react';

/**
 |--------------------------------------------------------------------------
 | The modal props types.
 |--------------------------------------------------------------------------
 */
export interface IModalProps {

    /**
     |--------------------------------------------------------------------------
     | The modal isShow parameter. (Required)
     |--------------------------------------------------------------------------
     */
    isShow: boolean;

    /**
     |--------------------------------------------------------------------------
     | The modal children node. (Required)
     |--------------------------------------------------------------------------
     |
     | Note: You need place react node in children parameter.
     |
     */
    children: ReactNode;

    /**
     |--------------------------------------------------------------------------
     | The modal on hide event. (Not required)
     |--------------------------------------------------------------------------
     */
    onHide?: (e: MouseEvent) => void;

}
